    private void updateAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAccountButtonActionPerformed
        new UpdateAccountFrame().setVisible(true);
        File accounts = new File("accounts.txt");

        try{
            if(!accounts.exists()) {
                accounts.createNewFile();
            }

            FileReader accountsFR = new FileReader(accounts);
            BufferedReader reader = new BufferedReader(accountsFR);
            String line;
            while((line=reader.readLine())!=null) {
                StringTokenizer strTokenizer = new StringTokenizer(line, ",");
                String email = "" + strTokenizer.nextElement();
                if(userEmail.equals(email)) {
                    String name = "" + strTokenizer.nextElement();
                    String age = "" + strTokenizer.nextElement();
                    String password = "" + strTokenizer.nextElement();
                    UpdateAccountFrame.nameTF.setText(name);
                    UpdateAccountFrame.ageTF.setText(age);
                    UpdateAccountFrame.passwordPF1.setText(password);
                    UpdateAccountFrame.emailTF1.setText(email);
                }
            }
            //CLOSING
            accountsFR.close();
            reader.close();

        } catch(IOException e){
            //JOptionPane.showMessageDialog(this, "IOException");
        }
        this.setVisible(false);
    }//GEN-LAST:event_updateAccountButtonActionPerformed

