                //EXCEPTION HANDLING
                if(!accountFile.exists()) {
                    accountFile.createNewFile();
                }

                //DELETING THE HISTORY
                FileReader fileR = new FileReader(accountFile);
                BufferedReader reader = new BufferedReader(fileR);
                StringBuffer buffer = new StringBuffer();
